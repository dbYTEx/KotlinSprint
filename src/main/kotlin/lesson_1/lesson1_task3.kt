package org.example.lesson_1

fun main() {
    val yearOfFlight: Int = 1963
    var hourOfFlight: String = "09"
    var minuteOfFlight: String = "07"

    println(yearOfFlight)
    println(hourOfFlight)
    println(minuteOfFlight)

    hourOfFlight = "10"
    minuteOfFlight = "55"

    print(hourOfFlight)
    print(":")
    print(minuteOfFlight)
}